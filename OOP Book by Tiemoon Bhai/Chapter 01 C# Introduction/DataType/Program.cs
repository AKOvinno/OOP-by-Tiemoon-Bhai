// String Data Type Example in C#
Console.WriteLine("---------------String Data Type Example in C#---------------");
string firstName = "Ashfaq Kadir";
string lastName = "Ovinno";
Console.WriteLine($"First Name: {firstName}");
Console.WriteLine($"Last Name: {lastName}");
string fullName = firstName + " " + lastName;
Console.WriteLine($"Full Name: {fullName}");
// Char Data Type Example in C#
Console.WriteLine("---------------Char Data Type Example in C#---------------");
char ch = 'A';
char ch2 = '4';
Console.WriteLine($"Character 1: {ch}");
Console.WriteLine($"Character 2: {ch2}");
// Numeric Data Types Example in C#
Console.WriteLine("---------------Numeric Data Types Example in C#---------------");
byte aByte = 12;
short aSmallValue = 45;
int noOfStudents = 100;
int numberOfRemainingOver = 23;
long population = 3400000000000000000;

double salaryAmount = 45000.50;
float valueOfSomething = 23.45F;
decimal divisionResult = 34.43m;

Console.WriteLine($"Byte Value: {aByte}");
Console.WriteLine($"Short Value: {aSmallValue}");
Console.WriteLine($"Integer Value (No of Students): {noOfStudents}");
Console.WriteLine($"Integer Value (Remaining Over): {numberOfRemainingOver}");
Console.WriteLine($"Long Value (Population): {population}");
Console.WriteLine($"Double Value (Salary Amount): {salaryAmount}");
Console.WriteLine($"Float Value (Value of Something): {valueOfSomething}");
Console.WriteLine($"Decimal Value (Division Result): {divisionResult}");
